import unittest
from src.player import Player

class TestPlayer(unittest.TestCase):
    def setUp(self):
        self.player_1 = Player('Robin', 'rock')
        self.player_2 = Player('Computer', 'scissors')

    def player_has_name(self):
        self.assertEqual('Robin', self.player_1.name)

    def player_has_choice(self):
        self.assertEqual('scissors', self.player_2.choice)